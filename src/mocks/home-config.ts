const STORAGE_KEY = 'home_page_config';

export function loadHomeConfig(): HomePageConfig {
  try {
    const stored = localStorage.getItem(STORAGE_KEY);
    return stored ? JSON.parse(stored) : defaultHomeConfig;
  } catch {
    return defaultHomeConfig;
  }
}

export function saveHomeConfig(config: HomePageConfig): void {
  localStorage.setItem(STORAGE_KEY, JSON.stringify(config));
}
